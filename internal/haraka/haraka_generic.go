func permute512Generic(state *[64]byte) {
	var s0, s1, s2, s3 [16]byte
	copy(s0[:], state[0:16])
	copy(s1[:], state[16:32])
	copy(s2[:], state[32:48])
	copy(s3[:], state[48:64])

	for r := range 6 {
		// Step 1: AES round with first 4 constants
		s0 = aesEnc(s0, rcGeneric[8*r+0])
		s1 = aesEnc(s1, rcGeneric[8*r+1])
		s2 = aesEnc(s2, rcGeneric[8*r+2])
		s3 = aesEnc(s3, rcGeneric[8*r+3])

		// Step 2: AES round with next 4 constants
		s0 = aesEnc(s0, rcGeneric[8*r+4])
		s1 = aesEnc(s1, rcGeneric[8*r+5])
		s2 = aesEnc(s2, rcGeneric[8*r+6])
		s3 = aesEnc(s3, rcGeneric[8*r+7])

		// Step 3: Mixing
		mix512(&s0, &s1, &s2, &s3)
	}

	copy(state[0:16], s0[:])
	copy(state[16:32], s1[:])
	copy(state[32:48], s2[:])
	copy(state[48:64], s3[:])
}
