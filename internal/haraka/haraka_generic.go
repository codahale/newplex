func aesEnc(state, key [16]byte) [16]byte {
	return aesni.AESENC(state, key)
}

// mix512 applies the mixing layer (transpose).
func mix512(s0, s1, s2, s3 *[16]byte) {
	// Treat each block as 4 uint32s.
	// We want to transpose the 4x4 matrix of uint32s.
	// Input:
	// s0: c00 c01 c02 c03
	// s1: c10 c11 c12 c13
	// s2: c20 c21 c22 c23
	// s3: c30 c31 c32 c33
	//
	// Output:
	// s0: c00 c10 c20 c30
	// s1: c01 c11 c21 c31
	// s2: c02 c12 c22 c32
	// s3: c03 c13 c23 c33

	row0 := sliceToU32s(s0)
	row1 := sliceToU32s(s1)
	row2 := sliceToU32s(s2)
	row3 := sliceToU32s(s3)

	// Transpose
	// Col 0 -> s0
	binary.LittleEndian.PutUint32(s0[0:4], row0[0])
	binary.LittleEndian.PutUint32(s0[4:8], row1[0])
	binary.LittleEndian.PutUint32(s0[8:12], row2[0])
	binary.LittleEndian.PutUint32(s0[12:16], row3[0])

	// Col 1 -> s1
	binary.LittleEndian.PutUint32(s1[0:4], row0[1])
	binary.LittleEndian.PutUint32(s1[4:8], row1[1])
	binary.LittleEndian.PutUint32(s1[8:12], row2[1])
	binary.LittleEndian.PutUint32(s1[12:16], row3[1])

	// Col 2 -> s2
	binary.LittleEndian.PutUint32(s2[0:4], row0[2])
	binary.LittleEndian.PutUint32(s2[4:8], row1[2])
	binary.LittleEndian.PutUint32(s2[8:12], row2[2])
	binary.LittleEndian.PutUint32(s2[12:16], row3[2])

	// Col 3 -> s3
	binary.LittleEndian.PutUint32(s3[0:4], row0[3])
	binary.LittleEndian.PutUint32(s3[4:8], row1[3])
	binary.LittleEndian.PutUint32(s3[8:12], row2[3])
	binary.LittleEndian.PutUint32(s3[12:16], row3[3])
}

func sliceToU32s(b *[16]byte) [4]uint32 {
	return [4]uint32{
		binary.LittleEndian.Uint32(b[0:4]),
		binary.LittleEndian.Uint32(b[4:8]),
		binary.LittleEndian.Uint32(b[8:12]),
		binary.LittleEndian.Uint32(b[12:16]),
	}
}

