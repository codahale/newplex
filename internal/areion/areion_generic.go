func permute512Generic(state *[64]byte) {
	var blocks [4][16]byte
	for i := range 4 {
		copy(blocks[i][:], state[i*16:(i+1)*16])
	}

	for r := range 15 {
		i0 := r % 4
		i1 := (r + 1) % 4
		i2 := (r + 2) % 4
		i3 := (r + 3) % 4

		x0 := blocks[i0]
		x1 := blocks[i1]
		x2 := blocks[i2]
		x3 := blocks[i3]

		rc := roundConstantsGeneric[r]
		zero := [16]byte{}

		// x1New = AESENC(x0, x1) -> F0(x0) ^ x1
		x1New := aesni.AESEnc(x0, x1)

		// x3New = AESENC(x2, x3) -> F0(x2) ^ x3
		x3New := aesni.AESEnc(x2, x3)

		// x0New = AESENCLAST(x0, 0) -> F1(x0)
		x0New := aesni.AESEncLast(x0, zero)

		// x2New = AESENC(AESENCLAST(x2, rc), 0) -> F3(x2)
		tmp := aesni.AESEncLast(x2, rc)
		x2New := aesni.AESEnc(tmp, zero)

		blocks[i0] = x0New
		blocks[i1] = x1New
		blocks[i2] = x2New
		blocks[i3] = x3New
	}

	for i := range 4 {
		srcIdx := (i + 3) % 4
		copy(state[i*16:(i+1)*16], blocks[srcIdx][:])
	}
}
